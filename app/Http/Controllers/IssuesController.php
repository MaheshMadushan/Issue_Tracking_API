     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'title' => 'required',
            'body' => 'required',
            'uuid' => 'required'
        ]);

        // store Issue
        $issue = new Issue();
        $data = $request->all();
        $issue->title = $data["title"];
        $issue->body = $data["body"];$slug = Str::slug($data["title"]." ".$data["uuid"], '-');$issue->uuid = $data["uuid"];
        $issue->slug = $slug;
        $issue->save();
        return $slug." stored";
    }

